# Практическое задание по уроку "Строки и индексация строк"
example = "это моя строка"
print(example[0])   #  первый символ
print(example[-1])  #  последний символ
print(example[7:13]) #  символы с 8 по 14
print(example[::-1])  #  строка наоборот
print(example[::2]) # каждый второй символ этой строки
