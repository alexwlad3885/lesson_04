# функция распечатывания параметра
def print_params(params_):
    print("1 раз: ", params_)
    print("2 раз: ", params_)


# вызов функции первый раз
print_params("Эх раз!")
# вызов функции второй раз
print_params("Ещё раз!")
# вызов функции третий раз
print_params("Ещё много, много раз!")
